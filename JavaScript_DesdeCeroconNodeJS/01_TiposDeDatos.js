/**
 * Tipos de datos
 */
//numero -number
console.log(27);
console.log(27.56);

//Texto -String
console.log("Luis Garcia");
console.log("Jesus Omar");
console.log(`Hola , soy gabriel`);

//valores logicos-boolean
console.log(true);
console.log(false);

//indefinido -undefined
console.log(undefined);

//null
console.log(null);

/**
 * Tipos de datos estructurales
 */

//Objeto --object
console.log({
  nombre: "Luis",
  edad: 27,
  fechaDeNacimiento: null,
  esAlto: false,
  ciudades: ["Quito", "Buenos Aires", "Nueva York"],
  direccion: { callePrincipal: "Siempre viva" },
});

//Arreglo --array
console.log(["Mexico", "Venezuela", "Ecuador"]);
console.log([21, true, { nombre: "gabriel" }, [1, 2, 3, 4, 5]]);
