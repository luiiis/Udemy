/**
 * Operadores aritmeticos
 * +,-,*,/,%,**
 */
console.log(2+2);
console.log(2-2);
console.log(2*2);
console.log(2/2);
console.log(2%2);
console.log(2**2);

//Operadores de incremento (++)
var numero =0;
console.log(++numero);//1
console.log(numero++);//1 se incrementa despues de asignarse
console.log(numero);//2


//operador de decremento(--)
var numero =3;
console.log(--numero);
console.log(numero--);
console.log(numero);